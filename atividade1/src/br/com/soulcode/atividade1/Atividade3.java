package br.com.soulcode.atividade1;

public class Atividade3 {
    public static void main(String[] args) {
        int numero1 = 10;
        int numero2 = 5;

        int soma = numero1 + numero2;
        System.out.println("A soma de " + numero1 + " e " + numero2 + " é igual a " + soma);

        int multiplicacao = numero1 * numero2;
        System.out.println("A multiplicação de " + numero1 + " por " + numero2 + " é igual a " + multiplicacao);


        double divisao = (double) numero1 / numero2;
        System.out.println("A divisão de " + numero1 + " por " + numero2 + " é igual a " + divisao);
    }
}

